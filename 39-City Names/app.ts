//Creating a function with parameters which return a values  in string
function cityCountry(city : string, coutry : string) : string{
    return `${city} , ${coutry}`

};

//Calling a function and print the returned value
console.log(cityCountry("Karachi", "Pakistan"))

console.log(cityCountry("Riyadh", "Saudi Arabia"));

console.log(cityCountry("Istanbul", "Turkey"));
